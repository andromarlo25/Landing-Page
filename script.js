// Carousel functionality for Clinic Management System
const clinicCard = document.querySelector('.clinic-carousel-card');
if (clinicCard) {
  const slides = clinicCard.querySelectorAll('.carousel-slide');
  const prevBtn = clinicCard.querySelector('.carousel-prev');
  const nextBtn = clinicCard.querySelector('.carousel-next');
  let currentSlide = 0;

  function showSlide(idx) {
    slides.forEach((slide, i) => {
      slide.classList.toggle('active', i === idx);
    });
  }

  prevBtn.addEventListener('click', (e) => {
    e.stopPropagation();
    currentSlide = (currentSlide - 1 + slides.length) % slides.length;
    showSlide(currentSlide);
  });
  nextBtn.addEventListener('click', (e) => {
    e.stopPropagation();
    currentSlide = (currentSlide + 1) % slides.length;
    showSlide(currentSlide);
  });
  // Optional: Reset to first slide on mouse leave
  clinicCard.addEventListener('mouseleave', () => {
    currentSlide = 0;
    showSlide(currentSlide);
  });
} 
