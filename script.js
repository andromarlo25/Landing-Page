// Typing effect for social links
function typeSocialLinks() {
    const socialLinks = document.querySelectorAll('.social-link');
    socialLinks.forEach(link => link.style.opacity = 0);
    let i = 0;
    function showNext() {
        if (i < socialLinks.length) {
            socialLinks[i].style.opacity = 1;
            socialLinks[i].style.transition = 'opacity 0.3s';
            i++;
            setTimeout(showNext, 250);
        }
    }
    showNext();
}

